package ProgramsOnVariables_DataTypes_Operators_IfElse;

public class SumOfNumbers {
	public static void main(String[] args) {
		System.out.println("The sum of 2 numbers is :::" + (12 + 2));
		System.out.println("The product of 2 numbers is :::" + (12 * 2));
		System.out.println("The differene between 2 numbers sum and 2 numbers product is :::" + ((12 + 2) - (12 * 2)));
	}
}
