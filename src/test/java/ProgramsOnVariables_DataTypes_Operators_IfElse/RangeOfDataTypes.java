package ProgramsOnVariables_DataTypes_Operators_IfElse;

public class RangeOfDataTypes {
	public static void main(String[] args) {
		System.out.println("Minimum range of byte datatype   : " + " " + Byte.MIN_VALUE);
		System.out.println("Maximum range of byte datatype   : " + " " + Byte.MAX_VALUE);
		System.out.println();
		System.out.println("Minimum range of short datatype  : " + " " + Short.MIN_VALUE);
		System.out.println("Maximum range of short datatype  : " + " " + Short.MAX_VALUE);
		System.out.println();
		System.out.println("Minimum range of int datatype    : " + " " + Integer.MIN_VALUE);
		System.out.println("Maximum range of int datatype    : " + " " + Integer.MAX_VALUE);
		System.out.println();
		System.out.println("Minimum range of long datatype   : " + " " + Long.MIN_VALUE);
		System.out.println("Maximum range of long datatype   : " + " " + Long.MAX_VALUE);
		System.out.println();
		System.out.println("Minimum range of float datatype  : " + " " + Float.MIN_VALUE);
		System.out.println("Maximum range of float datatype  : " + " " + Float.MAX_VALUE);
		System.out.println();
		System.out.println("Minimum range of double datatype : " + " " + Double.MIN_VALUE);
		System.out.println("Maximum range of double datatype : " + " " + Double.MAX_VALUE);
	}
}
