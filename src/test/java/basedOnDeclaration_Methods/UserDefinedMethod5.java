//WAP to check whether a character is LOWER CASE Letter or not.. 
package basedOnDeclaration_Methods;
public class UserDefinedMethod5 {
	public static void main(String[] args) {
		char ch = 'f';
		if (ch >= 'a' && ch <= 'z') {
			System.out.println(ch + " is a Lower case letter ");
		} else {
			System.out.println(ch + " is not a Lower case letter ");
		}
	}
}
