//WAP to display your name 10 times 
package com.qspider.WhileLoop;

public class WhileLoop1 {
	public static void main(String[] args) {
		int num = 1;
		String name = "Saisudha";
		System.out.println("Name is displayed for 10 times ");
		while (num <= 10) {
			System.out.println(name);
			// System.out.println("Saisudha");
			num++;
		}
	}
}
