// subString() is used to fetch/get part of String based on index/position..
package com.qsp.stringprograms;
public class subString {
	public static void main(String[] args) {
		String str = "Jagannath";
		System.out.println(str.substring(3));
		System.out.println(str.substring(3, 6));  // include 1st character and exclude last character
		System.out.println(str.substring(3, 7));  //
	}
}
